def get_planet_sign_interpretation(planet_name: str, sign_name: str) -> str:
    planet_interps = PLANET_SIGN_INTERPRETATIONS.get(planet_name, {})
    return planet_interps.get(sign_name, f"{planet_name}在{sign_name}的独特能量正在展现。")


def get_sign_interpretation(sign_name: str) -> Dict[str, Any]:
    return ZODIAC_SIGN_INTERPRETATIONS.get(sign_name, {})


def get_house_interpretation(house_number: int) -> Dict[str, Any]:
    return HOUSE_INTERPRETATIONS.get(house_number, {})


def get_aspect_interpretation(planet1: str, planet2: str, aspect_type: str) -> str:
    aspect_info = ASPECT_INTERPRETATIONS.get(aspect_type, {})
    planetary = aspect_info.get("planetary", {})
    
    p1 = planetary.get(planet1, {})
    if planet2 in p1:
        return p1[planet2]
    
    p2 = planetary.get(planet2, {})
    if planet1 in p2:
        return p2[planet1]
    
    description = aspect_info.get("description", "")
    return f"{planet1}与{planet2}形成{aspect_type}：{description}"


def generate_full_interpretation(chart_data: Dict[str, Any]) -> Dict[str, Any]:
    planets = chart_data.get("planets", [])
    aspects = chart_data.get("aspects", [])
    houses = chart_data.get("houses", {}).get("houses", [])
    ascendant = chart_data.get("ascendant", {})
    sun_sign = chart_data.get("sun_sign", {})
    moon_sign = chart_data.get("moon_sign", {})
    
    main_planets = [p for p in planets if p["name"] in [
        "太阳", "月亮", "水星", "金星", "火星", "木星", "土星", "天王星", "海王星", "冥王星"
    ]]
    
    planet_interpretations = []
    for planet in main_planets:
        sign = planet.get("zodiac", {}).get("sign", "")
        house = planet.get("house", 1)
        
        planet_interpretations.append({
            "planet": planet["name"],
            "planet_symbol": planet.get("symbol", ""),
            "sign": sign,
            "sign_symbol": planet.get("zodiac", {}).get("sign_symbol", ""),
            "house": house,
            "degree": planet.get("zodiac", {}).get("dms", {}).get("formatted", ""),
            "is_retrograde": planet.get("is_retrograde", False),
            "sign_interpretation": get_planet_sign_interpretation(planet["name"], sign),
            "house_interpretation": get_house_interpretation(house)
        })
    
    major_aspect_names = ["合相", "对分相", "四分相", "三分相", "六分相"]
    major_aspects = [a for a in aspects if a.get("aspect") in major_aspect_names]
    
    aspect_interpretations = []
    for aspect in major_aspects:
        aspect_interpretations.append({
            "planet1": aspect.get("planet1", ""),
            "planet1_symbol": aspect.get("planet1_symbol", ""),
            "planet2": aspect.get("planet2", ""),
            "planet2_symbol": aspect.get("planet2_symbol", ""),
            "aspect": aspect.get("aspect", ""),
            "aspect_symbol": aspect.get("aspect_symbol", ""),
            "orb": aspect.get("orb", 0),
            "actual_angle": aspect.get("actual_angle", 0),
            "interpretation": get_aspect_interpretation(
                aspect.get("planet1", ""),
                aspect.get("planet2", ""),
                aspect.get("aspect", "")
            )
        })
    
    element_counts = {"火": 0, "土": 0, "风": 0, "水": 0}
    quality_counts = {"开创": 0, "固定": 0, "变动": 0}
    
    for planet in main_planets:
        sign = planet.get("zodiac", {})
        element = sign.get("element", "")
        quality = sign.get("quality", "")
        
        if element in element_counts:
            element_counts[element] += 1
        if quality in quality_counts:
            quality_counts[quality] += 1
    
    dominant_element = max(element_counts, key=element_counts.get)
    dominant_quality = max(quality_counts, key=quality_counts.get)
    
    return {
        "basic_info": {
            "sun_sign": sun_sign.get("sign", ""),
            "sun_sign_symbol": sun_sign.get("sign_symbol", ""),
            "moon_sign": moon_sign.get("sign", ""),
            "moon_sign_symbol": moon_sign.get("sign_symbol", ""),
            "ascendant": ascendant.get("sign", ""),
            "ascendant_symbol": ascendant.get("sign_symbol", ""),
            "midheaven": chart_data.get("midheaven", {}).get("sign", ""),
            "midheaven_symbol": chart_data.get("midheaven", {}).get("sign_symbol", ""),
            "input": chart_data.get("input", {})
        },
        "planet_interpretations": planet_interpretations,
        "aspect_interpretations": aspect_interpretations,
        "element_analysis": {
            "counts": element_counts,
            "dominant": dominant_element,
            "dominant_info": ELEMENT_QUALITY_INTERPRETATIONS["elements"].get(dominant_element, {})
        },
        "quality_analysis": {
            "counts": quality_counts,
            "dominant": dominant_quality,
            "dominant_info": ELEMENT_QUALITY_INTERPRETATIONS["qualities"].get(dominant_quality, {})
        }
    }
