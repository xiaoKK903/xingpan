def search_cities_fallback(query: str, limit: int = 10):
    query_lower = query.strip().lower()
    
    results = []
    
    if query_lower in CITIES_EN:
        city_name = CITIES_EN[query_lower]
        if city_name in CITIES_DB:
            city = CITIES_DB[city_name].copy()
            city["display_name"] = f"{city_name}, {city.get('state', '')}, {city.get('country', '')}"
            results.append(city)
            return results[:limit]
    
    for city_name, city_data in CITIES_DB.items():
        if query_lower in city_name.lower():
            city = city_data.copy()
            city["display_name"] = f"{city_name}, {city.get('state', '')}, {city.get('country', '')}"
            results.append(city)
            if len(results) >= limit:
                break
    
    if not results:
        for en_name, cn_name in CITIES_EN.items():
            if query_lower in en_name:
                if cn_name in CITIES_DB:
                    city = CITIES_DB[cn_name].copy()
                    city["display_name"] = f"{cn_name}, {city.get('state', '')}, {city.get('country', '')}"
                    results.append(city)
                    if len(results) >= limit:
                        break
    
    return results[:limit]


def geocode_fallback(city: str):
    results = search_cities_fallback(city, limit=1)
    if results:
        city_data = results[0]
        return {
            "found": True,
            "city": city_data.get("name", city),
            "latitude": city_data.get("latitude", 0),
            "longitude": city_data.get("longitude", 0),
            "display_name": city_data.get("display_name", ""),
            "country": city_data.get("country", ""),
            "state": city_data.get("state", ""),
            "timezone": city_data.get("timezone", "Asia/Shanghai")
        }
    return None
